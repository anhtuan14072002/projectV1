                <h3>Suggested practice timeline for calligraphy beginners</h3>
                <p>As I mentioned in tip number three, you should move gradually from easier to more complex elements in your practice.</p>
                <p>So to recap all of these tips, here is how I envision the timeline for those just getting started –</p>
                <p>1. Pick a calligraphy style (script) you want to learn and practice.</p>
                <p>2. Buy the correct calligraphy pen as well as the essential supplies needed.</p>
                <p>3. Find a good study resource that includes the correct proportions and sizing, exemplar with a ductus, basic strokes, letter group division, etc. This could be a workshop, instructional book, online course, etc. </p>
                <p>4. Begin by studying the exemplar and by practicing the basic calligraphy strokes.</p>
                <p>5. Once you get comfortable with that, practice blocks of text and divide your practice into rhythmic and analytical sessions, shorter (consistent) daily sessions will yield better results than practicing twice a week.</p>
                <p>6. Parallel with that practice, challenge yourself to work on the final pieces to the best of your ability.</p>
                <p>7. As the weeks pass, keep practicing your foundations and train your “calligraphic eye.” Pay attention to rhythm, (negative) spacing, correct proportions (referring to your study resource), etc.</p>
                <p>8. Begin playing with more complex layouts and compositions. Learn the fundamentals of calligraphy flourishing and incorporate them into your practice.</p>
                <p>9. You can start noticing that personal style is taking form at this point. The more you practice, the more confident you become to evolve and experiment with your calligraphy art.</p>
