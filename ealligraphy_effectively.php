                <h3>6. Find a proper resource for the calligraphy style you want to practice. </h3>
                <p>Once you’ve picked out the calligraphy style you want to practice, finding a good resource for you to study from is crucial. </p>
                <p>The worst thing you can do is find a random alphabet from Google or Pinterest and copy the letters. </p>
                <p>If you can’t attend a workshop hosted by a professional calligrapher, a great alternative is a good calligraphy book. </p>
                <img src="images\li-HowToPractice6.jpg" alt="">
                <p>I’ve shared my <b>top 10 calligraphy books for beginners</b>, and in them, you’ll find all the needed information to get you started. </p>
                <p>On the Lettering Daily website, you’ll also find tutorials for various calligraphy styles.  </p>
                <p>Finding a good study resource will help you learn the script correctly, allowing you to progress faster. </p>

                <h3>7. Begin with the basic strokes.</h3>
                <img src="images\li-HowToPractice7.jpg" alt="">
                <p>Now that you have your tools and study resources, you can finally begin your practice. </p>
                <p>Before writing letters and words, it’s best to begin with <b> the basic calligraphy strokes. </b></p>
                <p>Remember what I said about moving gradually from easier to more complex elements? </p>
                <p>The basic strokes can vary depending on the calligraphy style you’ve chosen to practice. </p>
                <p>These basic strokes are usually the building blocks of letters, and it’s a great starting point if you have yet to gain experience. </p>
                <p>It will also help you better understand how to operate the tool since you’re using different movements than you would for your regular handwriting. </p>
                <h3>How do you know when to move from the basic strokes to actual letters?</h3>
                <p>This is a great question. </p>
                <p>If you can execute the same basic stroke three to five times in a row in the same way. </p>
                <p>This indicates that you’ve built the needed muscle memory to move on to the next step. </p>
                <img src="images\li-HowToPractice8.jpg" alt="">

                <h3>8. Practice letters in groups</h3>
                <p>Now that you’ve got some practice with the basic strokes, we can proceed to writing letters. </p>
                <p>Depending on the calligraphy style you’ve chosen to practice, you will notice that some letters share similarities. </p>
                <p>This allows us to divide the letters of the alphabet into various groups based on these similarities. </p>
                <p>Rather than practicing the alphabet in order, we practice it in groups based on similarities. </p>
                <p>By practicing the alphabet in letter groups, you will better understand the relationship between the basic strokes and each letter. </p>
                <p>A great example is <b>Sheila Waters’s book Foundations of Calligraphy</b>,particularly where she talks about the Foundational hand (btw. Great book, highly recommend it). </p>
                <img src="images\li-HowToPractice9.jpg" alt="">
                <p>In this book section, she presents the whole Foundational Hand divided into four main letter groups. </p>
                <p>She also talks about the parent letters o and n and how their shapes repeatedly appear in one way or the other across the entire alphabet. </p>
                <p>A great quote from her book is – “A coherent alphabet style must have unity of its parts.” </p>
                <p>A great quote from her book is – “A coherent alphabet style must have unity of its parts.” </p>
                <p>Pay close attention to the spacing and the positive and negative spacing. </p>
                <p>Not just between words but also between each letter pair. </p>
                <p>Obviously, as you’re just getting started, it will be difficult, but follow the process and keep showing up daily, and you will see progress. </p>
                <p><b>Note</b>– In calligraphy, an exemplar is an example alphabet used for learning a specific script. Usually, exemplars also need a ductus. The ductus is the name of the little arrows that indicate the sequence and direction of each stroke needed to form a specific letter. </p>

                <h3>9. Write and analyze your calligraphy. </h3>
                <p>This is also an excellent practice tip from Sheila’s book. </p>
                <p>She calls it rhythmic and analytical practice. </p>
                <p>As soon as you feel confident enough with single letters, move on to writing words and even shorter sentences. </p>
                <p>An excellent exercise I learned from attending a blackletter calligraphy workshop by <b>Luca Barcellona</b> was to write out big blocks of text. </p>
                <p>These could be individual words or even sentences. </p>
                <img src="images\li-HowToPractice10.png" alt="">
                <p>The idea is to keep the block of text simple and focus on executing the letterforms properly and maintaining rhythm and spacing. </p>
                <p>The additional idea is to write out pangrams. If you’re unfamiliar with the term, pangrams are sentences containing all of the alphabet’s letters. </p>
                <p>The most popular pangram is – The quick brown fox jumps over the lazy dog. </p>
                <img src="images\li-HowToPractice11.png" alt="">
                <p><b> <b>Here you can find a list of 20+ pangrams for your practice. </b></b></p>
                <p>So take out your ruler and pencil, draw some guidelines, and start practicing. </p>
                <p>If you make a mistake or miss a letter, let it be for now. </p>
                <p>Focus on maintaining a rhythm of writing and finish the page. </p>
                <p>Once you’re done with your rhythmic practice, it’s time to analyze your work.</p>
                <img src="images\li-HowToPractice12.png" alt="">
                <p>This can be done during the same practice session or even the next day. </p>
                <p>It’s essential to keep the style (script) exemplar next to you so you have a reference and can objectively criticize your work. </p>
                <p>This is a tremendously helpful strategy since you’re going deep into the details and issues you currently face. </p>
                <p>This method will allow you to find your weak spots and improve them. </p>
                <p>You might have issues with the letterforms, pen angles, proportions, consistency, interlinear spacing, etc. </p>
                <p>I also recommend writing down your weak spots in a diary or separate piece of paper so you can use them for your next rhythmic practice session. </p>
                <p>Doing this will make you much more conscious about where to pay more attention. </p>
                <p>Make sure to date your sessions so you can follow your progress over time. </p>
                <p><b>Quick Note</b> - make sure to do at least 5 minutes of warm-up drills before you begin writing. It takes a bit of time to warm up and adjust the fine motoric movements in your arm. </p>

                <h3>10. Work on final pieces</h3>
                <p>When I say final pieces, I mean creating artwork ready to be shared online or hung as a decorative piece on the wall.</p>
                <p>A piece of work that requires you to invest time and effort to create it.</p>
                <img src="images\li-HowToPractice13.png" alt="">
                <p>By doing these sorts of projects, you push yourself to create the best artworks that reflect your current skill level. </p>
                <p>These could be single letters, single words, or even multiple words, quotes, etc. </p>
                <p>Feel free to take more time to work on these projects. If you need a few days to complete such a project, that’s totally fine.</p>
                <p>Rushing to create something just to share it online makes no sense. </p>
                <p>After all, the goal here is pushing your limits and improving your skills. </p>
                <p>Not chasing empty praise or vanity metrics such as likes and followers online. </p>
                <img src="images\li-HowToPractice14.png" alt="">
                <h3>images\li-HowToPractice14.png</h3>
                <p>If you find the time to do one finished piece per week, or at least 2-3 each month, that’s a good start. </p>
                <p>The rest of the time, I would focus on practicing both the hand and the eye by doing the drills mentioned above. Particularly the rhythmic and analytical practice along with the blocks of text. </p>
                <h3>Bonus tip – Train your calligraphic eye.</h3>
                <p>This tip is different but, in a way, one of the most important on the whole list. </p>
                <p>It has had a massive impact on the way I create my artwork. </p>
                <p>This tip was inspired by concepts already talked about by hand lettering artists <b>Martina Flor </b> and <b>Ken Barber</b>. </p>
                <p>Both are outstanding and experienced professionals. </p>
                <p>Martina calls it training your typographic eye, while Ken refers to it as the power of observation. </p>
                <p>Martina calls it training your typographic eye, while Ken refers to it as the power of observation. </p>
                <p>Training your arm and building the muscle memory required to produce a straight line isn’t the biggest challenge. </p>
                <p>It’s also not about being able to create the perfect single letter. Rather making a whole piece work as a unity.</p>
                <p>Learning how to observe and analyze a piece and determine what is good or bad about it is a fundamental skill that takes years to develop. </p>
                <p>Creating artwork with proper understanding and confidence is something im aspiring to do more than anything else. </p>
                <p>Next time you see an artwork you like, ask yourself what makes this piece good.  </p>
                <p>Is it the composition, the rhythm, the well-balanced counters, the consistency of the letterforms, etc?</p>
                <p>You can do the same thing with artwork that you don’t like. </p>
                <p>Why not? What makes it so wrong? Are you able to answer with some concrete understanding?</p>
                <p>Try to pay attention to the little details and nuances. The more you do it, the better you become at it. </p>
                <p>And before you know it, you’ll start using the same approach to your work, which will inevitably improve it. </p>
                <p>Try to be aware of this thing next time you create or look at a calligraphy piece. </p>
                <h3>Bonus tip 2 – Seek feedback from more experienced calligraphers</h3>
                <p>This tip could also have a massive impact on your progress. </p>
                <p>I understand that some might feel uncomfortable sharing their work and having someone more experienced take it apart. </p>
                <p>However, if done correctly, you could learn a lot.</p>
                <p>I always welcome feedback as long as it’s constructive, and you should embrace the same approach. </p>
                <p>I highly recommend contacting more experienced calligraphers and asking for feedback on your work. </p>
                <p>You will be surprised to see how many are willing and happy to help people interested in learning and improving. </p>
                <p>Im one of those people, so if you would like me to give you some constructive feedback, don’t hesitate to reach out 🙂</p>
                <p>I’ve done this multiple times with various people via Instagram, email, etc. And almost every single time I received great feedback that has helped me tremendously with my calligraphy practice.</p>
