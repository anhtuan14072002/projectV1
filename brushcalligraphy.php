                <p><b>Note : </b>Another term used in calligraphy for lowercase letters is minuscules.</p>
                <p>So if you are entirely new to calligraphy, I highly recommend you check out the basic calligraphy strokes tutorial before attempting to create letters, words, and sentences in calligraphy. </p>
                <p>By implementing the basic strokes into your calligraphy practice routine, you will develop a strong foundation that will help you improve much faster.</p>

