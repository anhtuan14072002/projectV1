                <h3>Brush calligraphy alphabet step by step from a to z (+ video)</h3>
                <p>If you look at the minuscule letters of the alphabet, you will notice that many letters are similar. </p>
                <p>That’s precisely why we divide letters into separate pieces and call them basic calligraphy strokes. </p>
                <p>
                That’s precisely why we divide letters into separate pieces and call them basic calligraphy strokes. 
                </p>
                <p>Instead, we divide the minuscule letters into groups based on their similarities. </p>
                <p>By doing this group division, we can practice the alphabet more effectively.</p>
                <p>Therefore, I have separated this alphabet into four groups. </p>
                <p>Aside from individual images with a ductus (the little red direction arrows), I also created a demonstration video for each group.</p>
                
                <h4>Brush calligraphy alphabet group 1 – i, u, t, j, y, v, w, r, n, m</h4>
                <p>This is the easiest group of letters.</p>
                <p>These letters are based predominantly on the underturn basic calligraphy stroke.</p>
                <p>Again, if you are unfamiliar with the basic calligraphy strokes, I highly recommend you check out that tutorial first. </p>
                <p>
                Let’s take a close look at each letter.</p>
                <p>You can also check out the demonstration video –</p>
                <iframe width="640" height="360" src="https://www.youtube.com/embed/wYqqfBmy3uA" title="Brush Calligraphy Alphabet - A to Z (Lowercase Part 1)" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" referrerpolicy="strict-origin-when-cross-origin" allowfullscreen></iframe>
                
                <h4>The letter i </h4>
                <p>The letter i begins with an entry stroke and then with an underturn.</p>
                <p>I like to make a little flick on top, but you can also make a little dot.</p>
                <img src="images\BrushCalligraphyAlphabet5.jpg" alt="">

                <h4>The letter u</h4>
                <p>Very similar to the letter i.</p>
                <p>We just need to add a second underturn next to it.</p>
                <p>Try to maintain an equal amount of space in your counters for more consistency.</p>
                <img src="images\BrushCalligraphyAlphabet6.jpg" alt="">
                <h4>The letter t</h4>
                <p>The letter t starts with an entry stroke.</p>
                <p>After that, you just add an extended underturn.</p>
                <p>Although the letter t is an ascending letter, it doesn’t extend to the ascending line but rather halfway to it.</p>
                <p>Finish it off with either a straight or curvy crossbar.</p>
                <p>The crossbar is usually placed on the waistline or slightly above it. You can place it higher up if you’re doing some <b>calligraphy flourishing. </b></p>
                <img src="images\BrushCalligraphyAlphabet7.jpg" alt="">

                <h4>The letter j</h4>
                <p>With the j, you also start with an entry stroke.</p>
                <p>However, instead of an underturn it has a descending loop.</p>
                <p>Finish it off with either the flick or a dot. </p>
                <img src="images\BrushCalligraphyAlphabet8.jpg" alt="">
                <h4>The letter y </h4>
                <p>The y is a combination of the letter i and j.</p>
                <p>So, an entry stroke, an underturn, and a descending loop. </p>
                <img src="images\BrushCalligraphyAlphabet9.jpg" alt="">
                <h4>The letter v</h4>
                <p>Begin with an entry stroke and add an underturn that goes up to the waistline.</p>
                <p>Finish it off by adding a little stroke known as the comma dot</p>
                <p>It’s basically like a smaller version of an underturn, which should fit inside the letter. </p>
                <img src="images\BrushCalligraphyAlphabet10.jpg" alt="">
                <h4>
                The letter w</h4>
                <p>It’s similar to the letter u.</p>
                <p>So, an entry stroke, two underturns, and a comma dot at the end.</p>
                <p>A tip to make the w more balanced is to make the second underturn slightly larger than the first one.</p>
                <p>This way, when you put the comma dot, the two underturns are optically adjusted and appear to have the same negative space. </p>
                <img src="images\BrushCalligraphyAlphabet11.jpg" alt="">
                <h4>The letter r </h4>
                <p>The r is a bit unique.</p>
                <p>Begin with an entry stroke that is not slanted as much as usual.</p>
                <p>Then for the thicker downstroke, begin slightly above the waistline. </p>
                <p>Apply pressure briefly, release it right at the waistline and add pressure again to form an underturn stroke.</p>
                <p>At first, it might be tricky to get this pressure, release, and pressure combination all in one take, but it’s easier than it looks. </p>
                <img src="images\BrushCalligraphyAlphabet12.jpg" alt="">
                <h4>The letter n</h4>
                <p>Instead of an underturn, with the n, you’ll start with an overturn stroke.</p>
                <p>Then, you’ll finish it with a compound curve right after that. </p>
                <img src="images\BrushCalligraphyAlphabet13.jpg" alt="">
                <h4>The letter m </h4>
                <p>Similar to the n, but with two underturns at the beginning followed by a compound curve at the end.</p>
                <p>Again, maintain a similar amount of negative space (aka open counters) for better consistency. </p>
                <img src="images\BrushCalligraphyAlphabet14.jpg" alt="">
                <p><b>Quick Note:</b>  the entry stroke comes only at the first letters of words. So when we connect letters, we lose that entry stroke. However, it’s good to keep it when you practice single letters like this. </p>
                <h4>Brush calligraphy alphabet group 2 – l, h, b, k, f</h4>
                <p>The second group focuses on the basic calligraphy stroke known as the ascending loop.</p>
                <p>You can also check out the demonstration video –</p>
                <iframe width="640" height="360" src="https://www.youtube.com/embed/s2hrUpt_P5k" title="Brush Calligraphy Alphabet - A to Z (Lowercase Part 2)" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" referrerpolicy="strict-origin-when-cross-origin" allowfullscreen></iframe>
                <h4>The letter l </h4>
                <p>Begin with a thin upstroke from the waistline up to the ascending line.</p>
                <p>Remember that this upstroke should be curved and more slanted than the angle lines.</p>
                <p>Curve it at the ascending line and gradually apply pressure as you reach the baseline.</p>
                <p>Before the baseline, slowly release the pressure and come back up with a thin upstroke. Similar to the underturn stroke. </p>
                <img src="images\BrushCalligraphyAlphabet15.jpg" alt="">
                <h4>The letter h</h4>
                <p>The letter begins with an entry stroke, followed by an ascending loop.</p>
                <p>Then finally, add a compound curve. </p>
                <img src="images\BrushCalligraphyAlphabet16.jpg" alt="">
                <h4>The letter b</h4>
                <p>Begin with an entry stroke, add an ascending loop, and finish with a reversed oval stroke.</p>
                <p>I like to do my reverse ovals with a small terminal, but you can also do a closed oval shape. </p>
                <img src="images\BrushCalligraphyAlphabeThe thin upstroke comes back to the middle of the x-height.t17.jpg" alt="">
                <h4>The letter k</h4>
                <p>Like the previous letters, begin with an entry stroke and add an ascending loop.</p>
                <p>However, with the letter k, we add a unique shape that resembles a small-sized capital letter, R.</p>
                <p>The loop is smaller than the leg.</p>
                <img src="images\BrushCalligraphyAlphabet18.jpg" alt="">

                <h4>The letter f</h4>
                <p>It’s a unique letter of the alphabet as it has both an ascending and descending loop (almost).</p>
                <p>I usually dont extend the descending loop to the descending line because otherwise, the letter becomes too big and dominant in a word/sentence. Instead, I lower it ¾ of the way.</p>
                <p>The thin upstroke comes back to the middle of the x-height.</p>
                <img src="images\BrushCalligraphyAlphabet19.jpg" alt="">
                <h4>Brush calligraphy alphabet group 3 – a, d, g, o, c, e, q</h4>
                <p>The third alphabet group focuses on the oval shape.</p>
                <p>The oval shape is one of the most challenging basic strokes in calligraphy.</p>
                <p>However, with proper practice, you’ll get it in no time.</p>
                <p>You can also check out the demonstration video –</p>
                <iframe width="640" height="360" src="https://www.youtube.com/embed/KtanYOq33ec" title="Brush Calligraphy Alphabet - A to Z (Lowercase Part 3)" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" referrerpolicy="strict-origin-when-cross-origin" allowfullscreen></iframe>
                <h4>The letter a </h4>
                <p>The letter a is pretty simple.</p>
                <p>Start with an entry stroke, add an oval, and finish with an underturn.</p>
                <img src="images\BrushCalligraphyAlphabet20.jpg" alt="">

                <h4>The letter d</h4>
                <p>The letter d starts with a thin upstroke, an oval, and an extended underturn.</p>
                <p>Slightly higher than the extended underturn we had for the letter t. </p>
                <img src="images\BrushCalligraphyAlphabet21.jpg" alt="">

                <h4>The letter g</h4>
                <p>Begins with an entry stroke, an oval, and a simple descending loop. </p>
                <img src="images\BrushCalligraphyAlphabet22.jpg" alt="">

                <h4>The letter o</h4>
                <p>The o is an oval shape with a comma dot on the upper right.</p>
                <p>As with the v and the w, the comma dot should be placed inside the letter</p>

                <img src="images\BrushCalligraphyAlphabet23.jpg" alt="">

                <h4>The letter c</h4>
                <p>The c begins with an entry stroke.</p>
                <p>Then place your pen on the waistline and gradually start applying pressure to form a gradual curve.</p>
                <p>Remember to reduce the pressure gradually before the baseline as you curve back up in a thin upstroke.</p>
                <p>The thickest part of the curve should be somewhere in the middle of the x-height.</p>
                <p>Finish with a thin line that comes down from the top. You can add a little terminal here, as I like to do it. </p>
                <img src="images\BrushCalligraphyAlphabet24.jpg" alt="">

                <h4>The letter e </h4>
                <p>The same as the c.</p>
                <p>Just extend the thin upstroke to the thick downstroke to close the loop.</p>
                <p>I like to close it slightly higher than the middle. </p>
                <img src="images\BrushCalligraphyAlphabet25.jpg" alt="">

                <h4>The letter q</h4>
                <p>Start with a thin upstroke, add a curve, and finally, a reversed descending loop.</p>
                <p>The reversed descending loop returns to the baseline and bounces off as a connecting thin upstroke. </p>
                <img src="images\BrushCalligraphyAlphabet26.jpg" alt="">
                <h4>Brush calligraphy alphabet group 4 – p, s, x, z</h4>
                <p>This last group is made of exception letters.</p>
                <p>Letters that dont fully share or have the basic calligraphy strokes in them.</p>

                <p>You can also check out the demonstration video –</p>
                <iframe width="640" height="360" src="https://www.youtube.com/embed/wBIPT8VhjJY" title="Brush Calligraphy Alphabet - A to Z (Lowercase Part 4)" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" referrerpolicy="strict-origin-when-cross-origin" allowfullscreen></iframe>
                <h4>The letter p</h4>
                <p>The p starts with an entry stroke followed by a long thick downstroke.</p>
                <p>I usually don’t extend the downstroke to the descender line, but if you want, you can do so.</p>
                <p>Finish it off with a reverse oval.</p>
                <p>As with the b, you can either add a terminal in the middle or completely close it off.   </p>
                <img src="images\BrushCalligraphyAlphabet27.jpg" alt="">
                <h4>The letter s </h4>
                <p>The letter s is one of the most tricky letters of the alphabet.</p>
                <p>A common struggle for beginners. Begin with an entry stroke.</p>
                <p>Then, like with the letter r, apply pressure slightly above the waistline.</p>

                <p>Next, release it on the waistline, and in a similar motion as the reverse curve, start applying pressure to form the bowl of the s.</p>
                <p>I like to end the s with a little terminal at the end, but you can leave it as a thin upstroke. </p>
                <img src="images\BrushCalligraphyAlphabet28.jpg" alt="">

                <h4>The letter x</h4>
                <p>Begin with a similar shape as the compound curve.</p>
                <p>The difference is that the swelled down stroke (in the middle) is slightly flattened.</p>
                <p>Finish it with a thin upstroke in the middle of the downstroke.</p>
                <img src="images\BrushCalligraphyAlphabet29.jpg" alt="">
                <h4>The letter z</h4>
                <p>The z seems like it’s made of an overturn and a descending loop.</p>
                <p>However, it’s slightly different.</p>
                <p>The overturn twists at the end, and you extend a descending loop that is more curved than straight. </p>
                <img src="images\BrushCalligraphyAlphabet30.jpg" alt="">

