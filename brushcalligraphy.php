                <h3>The “secret” behind calligraphy alphabet letters</h3>
                <p>Brush calligraphy letters may seem tricky and challenging at first, but if you learn and practice the “secret” behind it, you’ll see that’s not the case at all.</p>
                <p>The secret is – <b>The basic calligraphy strokes.</b></p>
                <p>In short, the basic calligraphy strokes are the building blocks for our calligraphy letters. </p>
                <p>It’s a set of 8 basic strokes that individually look like just some random lines. </p>
                <img src="images\BrushCalligraphyAlphabet2.jpg" alt="">
                <p>However, when they are placed together in a different order, they form (almost) all of the letters of the lowercase alphabet. </p>
