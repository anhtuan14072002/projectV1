        document.querySelectorAll('.faq_header').forEach(header => {
            header.addEventListener('click', () => {
                const subFaq = header.nextElementSibling;
                const icon = header.querySelector('i');

                // Hide all other sub_faq elements
                document.querySelectorAll('.sub_faq').forEach(faq => {
                    if (faq !== subFaq) {
                        faq.style.display = 'none';
                    }
                });

                // Reset all icons
                document.querySelectorAll('.faq_header i').forEach(otherIcon => {
                    if (otherIcon !== icon) {
                        otherIcon.classList.remove('bi-chevron-up');
                        otherIcon.classList.add('bi-chevron-down');
                    }
                });

                // Toggle current sub_faq and icon
                if (subFaq.style.display === 'block') {
                    subFaq.style.display = 'none';
                    icon.classList.remove('bi-chevron-up');
                    icon.classList.add('bi-chevron-down');
                } else {
                    subFaq.style.display = 'block';
                    icon.classList.remove('bi-chevron-down');
                    icon.classList.add('bi-chevron-up');
                }
            });
        });
    </script>
