                <h3>The most common issues when using the wrong calligraphy paper</h3>
                <p>Here are a few common issues you can encounter when you use the wrong calligraphy paper.</p>
                <p><b>1. Ink bleeding</b> occurs when the ink soaks on the paper and starts showing on the other side. It might even leave a mark on the page or surface underneath. </p>
                <img src="images\CalligraphyPaper2.jpg" alt="">
                <p><b>2. Ink feathering</b>‚Äì happens when the paper fibers absorb much of the ink and they dont hold it in place. It then starts spreading through capillary action, completely ruining the look of your calligraphy. </p>
                <img src="images\CalligraphyPaper3.jpg" alt="">
                <p><b>Note</b>‚Äì bleeding and feathering can also be caused by the ink. </p>
                <p><b>3. Paper fibers catching onto the nib </b>‚Äì when small pieces of paper start catching on the edges of your nib. This can happen both to broad edge and pointed nibs.</p>
                <img src="images\CalligraphyPaper4.jpg" alt="">
                <p><b>4. Lack of control </b>‚Äì some papers have texture on them. This is also known as the paper tooth. Depending on the tool you‚Äôre working with, paper with too much or too little texture (too smooth) can be both good and bad. I‚Äôll talk more about that later on.</p>
                <p><b>5. Fraying brush tips</b> ‚Äì specific issue related to felt tip brush pens. Rough paper fibers can damage the tips of your brush pens. Once that happens, you‚Äôre stuck with a crappy brush pen. </p>
                <img src="images\CalligraphyPaper6.jpg" alt="">
                <p><b>Note - </b> Not all brush pen tips are the same. I talk about this in my <b>best brush pens article. </b>For example, the felt tip brush pen of an <b>ABT Tombow</b> is much more sensitive than the small plastic tip of the <b>Tombow Fudenosuke</b> or <b>Pentel Fude Touch. </b>This means it will fray more easily if the wrong paper is used. </p>
                <img src="images\CalligraphyPaper7.jpg" alt="">
