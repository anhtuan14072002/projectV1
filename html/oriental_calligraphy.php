        <h2>Orental Calligraphy</h2>
        <p>Eastern calligraphy is an ancient and refined art form, expressed through the writing of beautiful
            characters. This art has flourished in East Asian cultures such as China, Japan, Korea, and Vietnam. Each
            country has its own styles and characteristics, but they all share fundamental principles of balance,
            harmony, and expressiveness. Here is an overview of Eastern calligraphy:

        </p>
        <h4>1. Chinese Calligraphy</h4>
        <p>Chinese calligraphy, known as "Shufa" (书法), is a significant part of Chinese culture. It includes various
            styles of writing such as Kaishu (Standard Script), Xingshu (Running Script), Caoshu (Cursive Script), and
            Zhuanshu (Seal Script). The primary tools of Chinese calligraphy are the brush, ink, inkstone, and xuan
            paper. Chinese calligraphy is not just a way of writing characters but also a form of meditation and a means
            of expressing the writer's soul.</p>
        <h4>2. Japanese Calligraphy</h4>
        <p>Japanese calligraphy, or "Shodo" (書道), is heavily influenced by Chinese calligraphy but has developed its own
            unique features. The writing style in Japan may include Kanji (Chinese characters) and Kana (syllabic
            scripts). While Kanji conveys strength and fluidity, Kana is more gentle and delicate. Japanese calligraphy
            emphasizes not only aesthetics but also the conveyance of the writer's emotions and spirit.</p>
        <h2>3. Korean Calligraphy</h2>
        <p>Korean calligraphy, known as "Seoye" (서예), also originated from Chinese calligraphy but has distinct
            characteristics. Korea has its unique Hangul alphabet, and Korean calligraphy combines Chinese characters
            and Hangul, creating a very special style. The tools and techniques of Korean calligraphy are similar to
            those of Chinese calligraphy, but the creativity and transformation in writing characters reflect the Korean
            cultural identity.

        </p>

        <h2>4. Vietnamese Calligraphy
        </h2>

        <p>Vietnamese calligraphy, or "Thư pháp Hán Nôm," uses Chinese characters and Nom characters, an ancient
            Vietnamese writing system. Vietnamese calligraphy is deeply influenced by Chinese calligraphy but has
            evolved to fit the Vietnamese language and culture. Modern Vietnamese calligraphy also uses the Latin-based
            Quoc Ngu script, adding to the richness and diversity of calligraphy art in Vietnam.

        </p>
        <h2>5. Tools and Techniques
        </h2>
        <ul>
            <li>Brush: The primary tool in Eastern calligraphy, made from animal hair such as goat, wolf, or rabbit.
            </li>
            <li>Ink: Traditionally made from soot and glue, known as "ink stick," which is ground with water on an
                inkstone.</li>
            <li>Inkstone: Used to grind the ink stick into liquid form before writing.</li>
            <li>Xuan Paper: Special paper used for calligraphy, known for its good absorbency.</li>
        </ul>
        <h2>6. Significance
        </h2>
        <p>Eastern calligraphy is not only a visual art but also a means to train the mind and spirit. It requires
            patience, delicacy, and deep understanding of culture and history. Calligraphy is seen as a symbol of
            elegance and intellect and is an integral part of East Asian cultural heritage</p>
        <h2>7. Modern Applications</h2>
        <p>
            Today, Eastern calligraphy is not only preserved as cultural heritage but also applied in various modern art
            forms such as graphic design, visual arts, and fashion. Calligraphy has also become a hobby and a means of
            stress relief and meditation in modern life.

            Thus, Eastern calligraphy is a rich and diverse art form, deeply reflecting the cultures and souls of East
            Asian peoples.
        </p>
