        <h4>Differences Between English and Chinese Calligraphy</h4>
        <p>Writing English calligraphy is very different from Chinese calligraphy.</p>
        <p>As someone who personally enjoys hand lettering, I know from experience that the style used for English
            calligraphy doesn’t carry over well into Chinese calligraphy.</p>
        <p>In English calligraphy, one of the basic elements requires down strokes to be thicker than up strokes,
            creating a contrast that is pleasing to the eye.</p>
        <p>However, these rules don’t apply for Chinese calligraphy.</p>
        <p>However, one important point that does (somewhat) carry over from English to Chinese is stroke order.</p>
        <p>Normally, writing words in English doesn’t require a certain stroke order.</p>
        <img src="/images/calli_or2.jpg" alt="">
        <p>Sure, maybe when you were learning to write back in elementary school, your teacher taught you to write
            letters a certain way.</p>
        <p>But in every day life, using a pencil or pen to write, it’s not noticeable whether you write the letter “V”
            with two down strokes, one up and one down, or both up!</p>
        <p>However, when writing calligraphy in English, the order does become a bit more important because the down
            strokes should be thicker and the up strokes thinner.</p>
        <p>In Chinese, stroke order is important, even in regular writing, but more so in calligraphy. In both cases, if
            your pen or pencil drags when writing quickly, these extra strokes can either be clues as to what the
            character should be, or misleading and confusing.</p>
        <p>Even when writing characters on my phone, I’ve noticed that I can write a really messy character, and my
            phone will still recognize it, as long as I’ve used the correct stroke order.</p>
        <p>Additionally, the way the brush drags across the paper changes the look of the character, so the correct
            stroke order and even direction of the stroke is essential!</p>
        <h4>Styles of Calligraphy</h4>
        <p>There are five basic styles of calligraphy.</p>
        <p>Each style is very distinct and has developed over the years.</p>
        <h4>Seal Script</h4>
        <p>The first style is Seal Style or Seal Script, also called 篆书 (zhuànshū).</p>
        <img src="/images/calli_or3.jpg" alt="">
        <p>Its name comes from the fact that this script was often engraved on stones or seals.</p>
        <p>This style was created during the Chin Dynasty.</p>
        <h4>Clerical Script</h4>
        <p>The second style is Clerical Script, or 隶书 (lìshū).</p>
        <p>This style was also using during the Chin Dynasty by government officials.</p>
        <img src="/images/calli_or4.jpg" alt="">
        <p>The word itself refers to servitude because this script was used by clerks working in prisons.</p>
        <p>This style is more efficient, simplifying some of the more complex curves of the Seal Style in favor of sharp
            bends, squared instead of round.</p>
        <p>This style is also a popular style for beginners to learn, as there aren’t as many strokes to learn</p>
        <h4>Running Script</h4>
        <p>Next is Running Script, or 草书 (cǎoshū).</p>
        <p>It was developed during the Han Dynasty.</p>
        <img src="/images/calli_or5.jpg" alt="">
        <p>Many great calligraphers used this style. This style is the most abstract and doesn’t require precise strokes
            like the other styles do.</p>
        <p>At the same time, it still has its own set of rules.</p>
        <h4>Walking Script</h4>
        <p>The fourth style is Walking Script, or 行书 (xíngshū).</p>
        <p>It was formed during the Han Dynasty.</p>
        <img src="/images/calli_or6.jpg" alt="">
        <p>This style is a combination of the Running Script, and the last style listed here, the Standard Script</p>
        <p>The Walking Script is smooth and connected, but not as much as the Running Style script.

            It’s also more simplified than Standard Script.</p>
        <h4>Standard Script</h4>
        <p>Last, but not least is the Standard Script/Style, or 楷书 (kǎishū)</p>
        <img src="/images/calli_or7.jpg" alt="">
        <p>This script is widespread throughout China.</p>
        <p>It is generally used in newspapers, textbooks, and government documents.

            The other scripts are not commonly seen .</p>
