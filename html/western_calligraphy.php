        <h2>Rustic Capitals</h2>
        <p>Along with the Square Roman Capitals, we can distinguish another very influential (similar) script called the
            Rustic Capital.</p>
        <p>Emerging in the same period, they were mostly painted with flat brushes or nibs as a more common script for
            everyday use –</p>
        <p>Opposed to the Square Roman Capitals that were being used for stone engraving and more formal occasions.</p>
