        <h2>The Copperplate Script
        </h2>
        <p>During the sack of Rome (1527), many scribes moved to the southern parts of France, where they developed the
            Italic Cancellaresca, which was later adapted to the French style Rhonde in the early 17th century.</p>
        <p>It wasn’t until the end of the 17th century when books with the French Rhonde script reached England, where
            John Ayres and William Banson refined their interpretations of the French script.</p>
        <p>During the following years and with the help of fellow writing masters such as Goerge Bickham, the English
            Roundhand spread across the world due to British Mercantile Trade.</p>
        <img src="/images/calli_wes18.jpg" alt="">
        <p>The name Copperplate originates back from when writing was engraved on plates made out of copper, which
            writing masters would use for printing samples of their beautiful work.</p>
        <p>It was being done this way because printing machines and movable type could not reproduce (yet) such
            complexity and variations of styles.</p>
        <p>This fantastic article by Sybille talks more in detail about how the name Copperplate became the standard
            term for this particular calligraphy style.</p>
        <img src="/images/calli_wes19.jpg" alt="">
        <h2>The Spencerian Script</h2>
        <p>This script originated in the United States and was developed in the 19th century by Platt Rogers Spencer.
        </p>
        <p>Platt Roger Spencer developed a style of penmanship that was used as a standard of writing for nearly a
            hundred years – up until the point the typewriting machine came along in the 20th century.</p>
        <p>What made this script so popular and influential was not only its beauty but also the functionality that lays
            behind the way you write it.</p>
        <h6>Fun fact</h6>
        <p>it is said that the Spencerian script served as an influence point for the Coca-Cola logotype, which we all
            know is one of the most iconic logotypes in the world.</p>
        <img src="/images/calli_wes20.jpg" alt="">
        <p>In this article, Dr. Joseph Vitolo talks more in-depth about the Copperplate and Spencerian scripts (as well
            as their differences) with plenty of examples. </p>
        <h2>The Modern Era</h2>
        <p>Although it seemed that machines had turned writing by hand obsolete, calligraphy took various directions,
            such as advertisement and demand in novelty for type design.</p>
        <p>We mark the modern era from the beginning of the 20th century with pioneers such as Edward Johnston and
            Rudolf Koch – also known as the fathers of modern calligraphy. </p>
        <p>Many new developments occurred in the last 100+ years, and it would drag this article out if we were to cover
            all of it. </p>
        <p>Instead, we are going to stick to the most impactful developments. </p>
        <img src="/images/calli_wes21.jpg" alt="">
        <h2>The Foundational Hand</h2>
        <P>This hand is set to be one of the most iconic alphabets in the whole realm of calligraphy alphabets due to
            its clearly defined characters.</P>
        <P>Edward Johnston devised it in the early twentieth century.</P>
        <P>The Foundational hand is an adaptation of the Carolingian script from the post-Roman era.
        </P>
        <img src="/images/calli_wes22.jpg" alt="">
        <P>According to Edward Johnston, the Foundational hand is the best script for a beginner to start learning the
            art of calligraphy.</P>
        <P>You can find a full step-by-step tutorial on the Foundational Hand on my website.</P>
        <H2>Neuland Capitals</H2>
        <p>Calligrapher and type designer Rudolf Koch designed these capitals in the early twentieth century. </p>
        <p>It’s an iconic and vigorous alphabet that many calligraphers still practice all over the world – ideal for
            large-scale and display work.</p>
        <h2>Modern Calligraphy Styles</h2>
        <p>Modern calligraphy is often associated with bouncy and whimsical brush calligraphy, often found on Instagram
            and Pinterest.</p>
        <p>However, it’s much more than that.</p>
        <p>Modern calligraphy refers to any writing style that does not adhere to the rules of traditional scripts.</p>
        <p>That doesn’t mean scribbling something on paper can be considered modern calligraphy.</p>
        <p>There are still standards of quality and beauty that can differentiate a skilled hand from someone just
            starting.</p>
        <p>Also, modern calligraphy is not just related to a single writing tool such as the brush pen.</p>
        <p>Modern calligraphy can be done with brushes, broad-edged pens, pointed nibs, markers, and more.</p>
        <p>You can create a modern calligraphy script that has its basis in the Copperplate script or even the Fraktur
            script.</p>
        <p>You can also flip the script and create something completely different.</p>
        <p>I think that’s exactly the beauty of modern calligraphy.</p>
