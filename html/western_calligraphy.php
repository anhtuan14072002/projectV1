        <h2>The Post-Roman Era</h2>
        <h3>The Uncial script</h3>
        <p>Several hundred years later (second or third century AD), a new script developed influenced by the Greek
            alphabet.</p>
        <p>The Uncial script was one of the early alphabets used in Christian texts.</p>
        <p>Aside from its beauty, it was a more practical and speedily written script, ideal, for example – copying the
            Holy Bible or other manuscripts.</p>
        <p>Bold and curved strokes characterize it, and it took fewer strokes to complete a letterform compared to the
            Roman Capitals.</p>
        <p>In the Uncial script, we slowly start noticing the development of lowercase letters (aka Minuscules).</p>
        <p>I find it extremely fascinating how the alphabet developed according to their need.</p>
        <p>We can distinguish a few notable variations of the Uncial script.
        </p>
        <p>The three most notable variations of Uncial calligraphy are – </p>
        <ul>
            <li>Uncial </li>
            <li>Half-Uncial</li>
            <li>Artificial Uncial</li>
        </ul>
        <p>Here you can see a magnificent example of the Insular Half-Uncial from the Book Of Kells dating back from the
            9th century.</p>
        <img src="/images/calli_wes3.jpg" alt="">
        <p>The Insular Half-Uncial is another variation of the Uncial script that originated in Ireland in the 7th
            century.</p>
        <p>The Artificial Uncial is the latest alternation of the Uncial script that emerged in the early 8th century.
        </p>
        <p>Its origins can be traced back to Southern England. The Artificial Uncial is a more elaborated version of the
            Uncial that introduces back the serif along with other more complex pen twists to achieve a more dynamic
            look from the letterforms.

        </p>
        <h2>The Early Middle Ages</h2>
        <p>As the Roman Empire spread across other European countries through the coming years, it eventually reached
            the northern parts of Europe, including England – bringing their language and the Roman alphabet.</p>
        <p>Over the following centuries, Western Europe entered the dark ages, followed by a decline of the Roman
            Empire.</p>
        <h2>The Beneventan Script
        </h2>
        <p>During this period, a new script developed that was influenced by the Uncial script, yet it developed its
            characteristics – one of them being ligatures (connecting strokes). </p>
        <p>The Beneventan script originated from the southern parts of Italy, and it was one of the longest-lasting
            Post-Roman scripts.</p>
        <img src="/images/calli_wes4.jpeg" alt="">
        <h2>The Carolingian Minuscule</h2>
        <p>One of the most important scripts of the medieval era was the Carolingian script which was developed during
            the rule of Charlemagne in the late eighth century.</p>
        <p>Charlemagne assigned Alcuin (Monk of York) to be his librarian and teacher and to copy both religious texts
            and manuscripts as well as other texts of science and literature dating from the Greek and Roman era.</p>
        <p>Alcuin, along with his team of scribes in a scriptorium based in Tours (France), developed a more streamlined
            script that served as a basis for the lowercase alphabet that we use today.</p>
        <img src="/images/calli_wes5.jpg" alt="">
