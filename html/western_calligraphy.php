        <h1>Western calligraphy & Latin alphabet – most influential calligraphy scripts + examples</h1>
        <p>These are some of the most influential historical periods that impacted the development of calligraphy in the
            context of the Latin alphabet (western calligraphy).</p>

        <ul>
            <li>Antiquity – Roman Empire</li>
            <li>Post Roman – Uncial & Insular period</li>
            <li>Middle ages </li>
            <li>Renaissance</li>
            <li>Baroque</li>
            <li>Modern</li>
        </ul>
        <p>I will review each of these periods and mention the most important scripts.</p>
        <h2>Antiquity – The Roman Empire</h2>
        <p>The Latin alphabet dates back to around 700 BC, and the Roman Square Capitals emerged around the first
            century AD in Rome.</p>
        <h2>Roman Capitals</h2>
        <p>Even though it’s one of many scripts from that era, it is by far the single most influential historical
            script.</p>
        <p>The beauty and the key of these letters lay in geometry and precision. One could say it reflects the finesse
            of the architecture and culture of that era.</p>
        <p>This script served as a fundamental basis for the Latin alphabet’s historical development and, essentially,
            how we communicate in writing today.</p>
        <img src="/images/calli_wes.jpg" alt="">
