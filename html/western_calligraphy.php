        <h2>The Insular Script</h2>
        <p>Another important mention of this era is the Insular script.</p>
        <p>A medieval script developed in Ireland, and it was influenced by the scripts from the Roman era.</p>
        <p>This particular script was influenced by Irish Christianity, which helped spread the script across England
            and continental Europe.</p>
        <p>One of the most well-known examples of the Insular script is the Book of Kells (among many others)

        </p>
        <img src="/images/calli_wes6.jpg" alt="">
        <img src="/images/calli_wes7.jpg" alt="">
        <h2>The Middle Ages – Gothic Period</h2>
        <p>Following the post-Roman era, a new script developed in Northern Europe (12th century).</p>
        <p>The Gothic scripts are VERY popular today, practiced by many calligraphers – several different scripts
            (styles) developed throughout this period.</p>
        <p>However, the changes were more noticeable this time in the new Gothic script.</p>
        <img src="/images/calli_wes9.gif" alt="">
        <p>Characterized by more compressed, thick, straight, and angular shapes, it was the opposite compared to the
            round shapes that characterized the Carolingian script.</p>
        <p>Although the Carolingian script was beautiful, it was time-consuming and took more space per page due to its
            round shapes and counterparts.</p>
        <p>Once again, the way of writing was adapted to the current need of the people – which at the time was a higher
            demand for handwritten books.</p>
        <p>Blackletter (Gothic) scripts also include different (similar) styles that evolved in different parts of
            Europe, such as –</p>
        <ul>
            <li>Textura Quadrata</li>
            <li>Textura Presciscus</li>
            <li>Batarde</li>
            <li>Fraktur</li>
            <li>Rotunda</li>
            <li>etc.
            </li>
        </ul>
        <img src="/images/calli_wes10.png" alt="">
        <p>You’ll find two step-by-step tutorials on how to get started with the Textura Quadrata script and the Fraktur
            script on my website.</p>
        <p>By now, the Gothic became the new way of writing across Europe.</p>
        <p>Books and legal documents were in high demand, and scribes were the only ones who could perform such tasks at
            that time.</p>
        <p>Scribes and book producers formed guilds, and soon enough, they had a monopoly over book production and legal
            documents – thus making them very powerful at the time.</p>
        <img src="/images/calli_wes11.jpg" alt="">
        <h2>All Good Things Must Come To An End…</h2>
        <p>One of the most crucial events in the second millennium was the invention of the printing press.</p>
        <p>A revolutionary invention by Johannes Gutenberg that brought prosperity to the world but put an end to the
            scribe.</p>
        <p>The printing press (and movable type) broke the monopoly, and simply no scribe could compete with a machine.
        </p>
        <p>The printing press could run over a hundred pages within an hour, while the scribe could only do one.</p>
        <p>This massive upgrade allowed millions of books to be printed in a very short period.</p>
        <img src="/images/calli_wes12.jpg" alt="">
        <h2>The Renaissance Era</h2>
        <p>As the year passed, the middle ages ended, and a new era arrived.</p>
        <p>An era of ‘’rebirth’’ for Western Europe and upcoming prosperity in major human activities, such as art,
            science, economy, etc.</p>
        <img src="/images/calli_wes13.jpg" alt="">
        <p>At the time, the gothic script that was developed in Northern Europe was the dominant script being used.</p>
        <p>However, that particular way of writing did not really resonate with the Italians.</p>
        <p>There was one scholar in particular who considered the Gothic to be rather unpractical and who openly
            criticized it in his manuscripts.</p>
        <p>His name was Francesco Petrarca.</p>
        <p>Petrarca described it as a script that appeals to the eye from a distance but creates fatigue from up close
            and that its purpose should be for something other than reading.</p>
        <p>Francesco also argued that writing should be clear, simple, and orthographically correct.</p>
        <p>These were the notions that gave birth to the Humanist minuscule.</p>
        <img src="/images/calli_wes14.jpg" alt="">
        <h2>The Humanist Minuscule</h2>
        <p>This is said to be one of the most significant and defining Western scripts.</p>
        <p>It was very popular in Italy at the time, and this script presented a model for the Venetian typeface.</p>
        <p>The Humanist minuscule was influenced by the Carolingian hand, as the scholars of that time period were
            particularly keen on perceiving themselves as the inheritors of the antique era.</p>
        <img src="/images/calli_wes15.jpg" alt="">
        <h2>The Cancellaresca Script (Italic)</h2>
        <p>The Cancellaresca script was another major update in the way people write.</p>
        <p>It was particularly innovative because it improved as a script in terms of functionality and speed of
            writing.</p>
        <p>Scholar Niccolò de’ Niccoli was dissatisfied with how laborious the Humanist minuscule was, so he created a
            slanted script that required fewer strokes and that could be joined with other letters.</p>
        <img src="/images/calli_wes16.jpg" alt="">
        <p>One of the biggest changes was made to the lowercase letter a, which was quite different from the one in the
            Humanist minuscule script.</p>
        <p>Another very important mention about the Cancellaresca script is that it served as a base for cursive writing
            that we teach and use today.</p>
        <h2>The Baroque era</h2>
        <p>After the period of Rennaisance, the Cancellaresca Corsiva provided the foundation for a new script to be
            developed. </p>
