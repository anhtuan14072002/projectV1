        <h6>
            Pens, Paper & Ink</h6>
        <p>Before you begin with calligraphy, you need to have the right pens, paper and ink. </p>
        <p>Having the right pen is most difficult because you need to change the angle of the nib once you buy it. Most
            pens come in the angle used in English calligraphy. That can be accomplished using sandpaper. Ustadh Muthana
            Suggested starting with 1800 grit sandpaper and polishing afterwards with 2000 grit or higher. </p>
        <p>You will need:</p>
        <ul>
            <li>Your pen, with a filled ink cartridge or converter
            </li>
            <li>Two sheets of sandpaper, one with a 1800 grit surface and the other with a 2000 grit surface.
            </li>
            <li>A small cup of warm water
            </li>
            <li>Tissue or paper towels
            </li>
        </ul>
        <p>How to do it:

        </p>
        <ul>
            <li>Lay the 1800 grit sandpaper on a flat surface.
            </li>
            <li>Dip the pen in water and make sure there is water on the sandpaper whenever you are sanding the nib.
            </li>
            <li>hold the pen at the desired angle. Make sure you do not hold it in the opposite angle!
            </li>
            <li>Keep hold of that angle and slowly sand away till the width of the nib is at the desired angle.
            </li>
            <li>Now you can polish the pen on the 2000 grit paper and pull it in different directions, so that it writes
                smoothly.
            </li>
            <li>Test the pen on a piece of paper to make sure it writes smoothly, and keep polishing till it does.
            </li>
            <li>Now you can start writing calligraphy.
            </li>
        </ul>
        <p>Now you can start writing calligraphy.
        </p>
