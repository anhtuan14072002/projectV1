        <h2>Arabic Calligraphy</h2>
        <p>Arabic calligraphy is an art form that uses handwriting in languages that use the Arabic alphabet or
            alphabets derived from Arabic. This includes Arabic, Persian, Ottoman, Afghan, Pakistani, and Indian
            calligraphy. Known in Arabic as "khatt Arabi" (خط عربي), it means Arabic lines and designs.</p>
        <p>The development of Islamic calligraphy is closely tied to the Quran, with chapters and verses from the Quran
            being the most common sources for Islamic calligraphy. While the Quran does not strictly prohibit the
            depiction of humans and animals in art, such paintings have traditionally been restricted in Islamic texts
            to prevent idolatry. In ancient times, artists often circumvented legal barriers by using tiny script to
            create outlines and images.</p>
        <p>Calligraphy in Islamic society is both a valued art form and consistent with religious ethics. An old Arabic
            proverb illustrates this view by emphasizing, "The purity of writing is the purity of the soul." However,
            Islamic calligraphy is not strictly limited to religious themes, subjects, or objects. Like other forms of
            Islamic art, calligraphy includes a range of works created from various texts.</p>

        <p>The prominence of calligraphy in Islamic art is not directly related to the tradition of aniconism. Instead,
            it reflects the central role of writing and texts in Islam. Notably, in one instance, the Prophet Muhammad
            is believed to have said, "The first thing God created was the pen."</p>
        <p>Islamic calligraphy developed from two main styles: Kufic and Naskh. Each style has several variations, as
            well as specific local styles. Before the invention of paper, papyrus and parchment were used for writing.
            In the 9th century, a flow of paper from China revolutionized calligraphy.
        </p>
