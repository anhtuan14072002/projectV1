                <p>The Speedball Textbook series has recently celebrated its 105th anniversary. </p>
                <p>Yup! 105 years! </p>
                <p>The Speedball Textbook was initially created by Ross F. George as an instructional manual. </p>
                <p>Last year the latest (25th) edition was released, and it has become so much more than that. </p>
                <p>First of all, let me say that the 25th edition is just out of this world. </p>
                <p>Not just because it contains a ton of great reference alphabets and jaw draping calligraphy artwork for inspiration. </p>
                <img src="images\10BestCalligraphyBooks29.jpg" alt="">
                <img src="images\10BestCalligraphyBooks30.jpg" alt="">
                <img src="images\10BestCalligraphyBooks31.jpg" alt="">
                <p>Seeing dozens of skillful calligraphers from all over the world coming together and creating this absolutely fantastic booklet is fantastic. </p>
                <img src="images\10BestCalligraphyBooks32.jpg" alt="">
                <p>I currently have the 23rd and the 25th edition. </p>
                <p>I would love to eventually increase my collection and have all of them. </p>
                <p>If you are interested in getting a Speedball Textbook, I would definitely recommend going with the 25th edition. </p>
                <p>It is filled, and I mean jam-packed, with many different styles and reference alphabets for you to practice from. </p>
                <p>You won’t just find calligraphy inspiration. There is also content on hand lettering, sign painting, improving handwriting, and more.</p>
                <img src="images\10BestCalligraphyBooks33.jpg" alt="">
                <img src="images\10BestCalligraphyBooks34.jpg" alt="">
                <img src="images\10BestCalligraphyBooks35.jpg" alt="">
