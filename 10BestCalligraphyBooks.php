                <h3>8. Medieval Calligraphy – Its History and Technique by Marc Drogin</h3>
                <img src="images\10BestCalligraphyBooks36.jpg" alt="">
                <p>If you like to read about calligraphy history (as much as I do), you’ll love this book. </p>
                <p>When I started researching the history of the Latin alphabet and its development throughout the years, I’ve come across many contradictions and a general lack of information. </p>
                <p>Unfortunately, there is a ton of information online, and there is a variety of interpretations when it comes to exact dates and correct nomenclature. </p>
                <p>Marc’s book is a fantastic read with many references, and you can really tell that it has been very well researched. </p>
                <img src="images\10BestCalligraphyBooks37.jpg" alt="">
                <img src="images\10BestCalligraphyBooks38.jpg" alt="">
                <p>The book is divided into two main sections. </p>
                <p>The first is the detailed historical overview, and the second is more of a “how-to” packed with historical references.</p>
                <img src="images\10BestCalligraphyBooks39.jpg" alt="">
                <p>I really love the first part of the book, where Marc describes the development of the Latin alphabet in great detail. </p>
                <p>All the way from its early development and the Roman Capitals to the peak of the gothic scripts.</p>
                <p>Each page is filled with detailed text and many reference images from various manuscripts. </p>
                <p>I particularly enjoyed reading about the way scribes used to live in that day and age and the funny story about Titivillus A.K.A. The Patron Demon Of Calligraphy 😀</p>
                <img src="images\10BestCalligraphyBooks40.jpg" alt="">
                <img src="images\10BestCalligraphyBooks41.jpg" alt="">
                <p>The only thing I wish was presented in more detail was the instructional part of the book. </p>
                <p>Don’t get me wrong, it contains great info and insights. I just wish it was presented better and more comprehensively, like, i.e., The Art Of Calligraphy. </p>

                <h3>9. Writing & Illuminating & Lettering by Edward Johnston </h3>
                <img src="images\10BestCalligraphyBooks42.jpg" alt="">
                <p>Would you believe it if I told you that this book was originally published in 1906?</p>
                <p>This book and its contents have been around for over 100 years. </p>
                <p>In case you haven’t heard of Edward Johnston, he is considered to be (among others) the father of modern calligraphy. </p>
                <p>He is famous for his design of the Johnston’s typeface and the London Underground system and his vast influence on a generation of calligraphers and type designers. </p>
                <p>His book, Writing & Illuminating & Lettering, is a must-have for any calligrapher/lettering artist. </p>
                <img src="images\10BestCalligraphyBooks43.jpg" alt="">
                <p>In Johnston’s book, you will find a ton of crucial information that will give you a much deeper knowledge about calligraphy. </p>
                <p>The book is divided into two parts.</p>
                <p>The first part focuses on writing and illuminating.</p>
                <p>Things such as setting up your writing environment, choosing the correct tools, how to hold the pen, spacing, creating (and planning) manuscripts, versals, gilding, and more.</p>
                <img src="images\10BestCalligraphyBooks44.jpg" alt="">
                <p>The second part is oriented toward more theoretical teaching of lettering and calligraphy.</p>
                <p>The qualities of good lettering, constructions, arrangement, proportions, layouts, compositions, and more.</p>
                <img src="images\10BestCalligraphyBooks45.jpg" alt="">
                <p>I really enjoy the rich content of the book’s second part, and I often come back to it.</p>
                <p>The way I read this book was like any other book. In the morning, with a cup of coffee, while traveling somewhere, etc.  </p>
                <p>It is not like other books on this list, one which you would use as an exemplar for you to copy from. </p>
                <p>It’s a smaller-sized book which makes it convenient for traveling and similar situations. </p>

