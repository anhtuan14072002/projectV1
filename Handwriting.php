                <h3>Step-by-step process to make the font.</h3>
                <p>The first thing we need to do is determine what type of font we’re going to create. </p>
                <p>I’ve already created a few fonts using my handwriting, like this right here, but for this article, I wanted to challenge myself a bit more and make a hand-lettered font from scratch. </p>

                <h3>Step 1 – Finding inspiration for your font</h3>
                <p>One of my favorite places to search for inspiration is Pinterest. </p>
                <p>I didn’t just find some inspiration. I found an overwhelming amount of inspiration. </p>
                <p>LIKE A LOT. </p>
                <p>I was super amazed by the massive variety of shapes and styles. </p>
                <img src="images\li-howtomake10.jpg" alt="">
                <p>If you want to do the same, check out my Pinterest board, where you’ll find all the images I saved.</p>
                <p>While cruising through endless styles and shapes, I decided to create a high-contrast display font. </p>
                <p>Display fonts are usually created with the intent to be used on larger sizes for titles and headings and not so much for longer chunks of text. </p>
                <img src="images\li-howtomake11.jpg" alt="">
                <p>
                One big reason I decided to go with a display font was that they look funkier, contain more variability, and, I guess creative freedom. </p>
                <p>With that in mind, I decided to create only capital letters. </p>

                <h3>Step 2 – Sketching your letters</h3>
                <p>I took my notes and ideas, opened the Procreate app, and started sketching. </p>
                <p>Sketching is the most crucial step of the whole process. </p>
                <p>Sketching (especially with a pencil) allows you to quickly and roughly lay down various ideas</p>
                <p>Often, even after 10-15 minutes of sketching, you surprise yourself with new ideas you didn’t have at the beginning of the process. </p>
                <p>I HIGHLY recommend you don’t skip this step. </p>
                <p>Here is my sketch for the font.</p>
                <img src="images\li-howtomake12.jpg" alt="">
                <p>You don’t need to Procreate for this. You can also sketch on paper.</p>
                <p>Or, if you’re super confident about the font style you want to create, you can start drawing your letters directly into Fontself. </p>

                <h3>Step 3 – Set up your sketch as a reference</h3>
                <p>Having a visual reference of your sketch in front of you will be of great help. </p>
                <p>This way, you don’t have to stop, go back, find your sketch, etc. You can get into a flow state and create the whole font rather quickly. </p>
                <p>The way I did this was by printing out the font. </p>
                <img src="images\li-howtomake13.jpg" alt="">
                <p>If you don’t have a printer, you could open up the image on your computer screen or phone. </p>
                <p>If you sketched your letters on paper, keep the paper in front of you. </p>

                <h3>If you sketched your letters on paper, keep the paper in front of you. </h3>
                <p>A lot of letters share similar parts, and by copying/pasting these parts, we make the process more efficient and increase the consistency of our letters. </p>
                <p>Thankfully the awesome selection tool in Fontself allows you to do exactly that. </p>
                <p>The most frequent part I can identify is the thick stem which appears in most letters. </p>
                <img src="images\li-howtomake14.jpg" alt="">
                <p>So, I will double tap on the square of the letter B, which will open a larger window allowing me to focus solely on that letter. </p>
                <p>A great feature for adding more details. </p>
                <p>I’m going to draw a line and hold it, which will trigger the automatic shape feature. The same way you would do it in Procreate.</p>
                <img src="images\li-howtomake15.jpg" alt="">
                <p>It works well with other shapes, and it’s super helpful to get nice clean lines. </p>
                <p>I add the little rounded serifs on each side, and I’ll fill the shape with color by simply tapping and holding onto it. </p>
                <img src="images\li-howtomake16.jpg" alt="">
                <p>Another handy feature. </p>
                <p>I can quickly undo and redo each stroke. Two fingers for undoing and three for a redo. Again, just as in Procreate. </p>
                <p>Ok, so I have my shape. Now, I’ll grab the selection tool, select the object, and copy it. </p>
                <p>The selection tool also allows you to move objects around, increase their size, rotate them, etc.</p>
                <p>Ok, once I’ve copied my stem, I’ll start pasting the shape in all the letter boxes that will use that shape in one way or another. </p>
                <img src="images\li-howtomake17.jpg" alt="">
                <p>I can also copy and paste similar letters such as the O, G, C, Q, etc. </p>
                <p>Of course, I don’t want to copy and paste every single detail; otherwise, I lose the organic touch that a hand-lettered font should have. </p>

                <h3>Step 5 – Create the rest of the letters</h3>
                <p>Now that we have copied and pasted the most frequently used parts, we can focus on creating all the other unique pieces and details. </p>
                <p>Keep an eye on proportions as well as the counter spaces.</p>
                <p>In the YouTube video, you can see the whole process.</p>
                <img src="images\li-howtomake18.jpg" alt="">
                <p>Having the reference image of your sketch will be of great help in this step. </p>
                <p>As I keep creating these letters, you can notice in the above preview how they’re starting to show and form the pangram – the quick brown fox jumps over the lazy dog. </p>

                <h3>Step 6 – Adjust the spacing of your letters</h3>
                <p>Good. Once you’re done drawing/writing your letters, it’s time to adjust the spacing. </p>
                <p>As mentioned, the spacing is controlled by a single setting for all letters rather than individually. </p>
                <img src="images\li-howtomake20.jpg" alt="">
                <p>You can also adjust the spacing between the words. </p>

