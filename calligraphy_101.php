                <h4>The Origins Of Calligraphy</h4>
                <p>Paleographers and historians believe the first alphabet evolved in Phoenicia around 1200 B.C.</p>
                <img src="/images/li_calligraphy101_8.png" alt="">
                <p>As Phoenicians merchants traveled and traded across the Mediterranean, the Greeks adopted their alphabet around the eighth century B.C.</p>
                <p>Soon after, the Etruscans adopted (and adapted) the Phoenician alphabet, which the Romans subsequently took.</p>
                <p>By the first century B.C., the Romans had already developed several scripts using the Latin alphabet, which we all know and use today.</p>
                <p>The word calligraphy derives from the Greek words “kallos” (beauty) and “graphein” (to write).</p>
                <p>According to Britannica.com, the word calligraphy entered most European languages by the end of the 16th century.</p>
                <p>This was initiated by the introduction of the printing press in the mid-15th century and thus created a clear distinction between handwriting and more elaborate forms of lettering.</p>
                <p>We often take it for granted, but the written word is one of the foundations of human civilization.</p>
                <p>Writing has allowed us to share and preserve information, ideas, and knowledge for thousands of years.</p>
                <p>The history of calligraphy is rich, with many evolutions throughout centuries.</p>
                <p>Below you can see a shorter general overview of how the Latin alphabet evolved throughout history.</p>
                <img src="/images/li_calligraphy101_9.png" alt="">
                <p>In case you want to learn more about the history of calligraphy, I’ve written a separate in-depth article on the history of calligraphy and how it evolved throughout the years. </p>
                <h4>How To Get Started With Calligraphy? The Three-Step Formula</h4>
                <p>Ok, now we know what calligraphy is, what it is used for, and where it comes from.</p>
                <p>Now it’s time to learn how to start learning and practicing calligraphy.</p>
                <p>If you’re a complete beginner, the best way to start learning calligraphy is by following the three-step formula :</p>
                <p><b>1. Choose a calligraphy style that you want to study and practice.</b></p>
                <p><b>2. Pick the correct calligraphy pen for the style you want to practice + the essential calligraphy tools.</b></p>
                <p><b>3. Find a proper study resource and create an effective practice routine.</b></p>
                <img src="/images/li_calligraphy101_10.png" alt="">
                <p>With that in mind, let’s have a closer look at each of these three key steps. </p>
                <h4>Step 1 – Choosing A Calligraphy Style </h4>
                <p>The first thing you should do is find the calligraphy style that appeals the most to you.</p>
                <p>This is the first step since the next step (picking the tools) will depend on your calligraphy style, aka script choice.</p>
                <img src="/images/li_calligraphy101_11.jpg" alt="">
                <p>I often get asked what is the easiest calligraphy style to start with, and the truth is that each style has its little nuances that will create challenges along the way.</p>
                <p>Some people might find working with a pointed nib easier, while others find it more comfortable with a broad-edged pen.</p>
                <p>To learn, understand, and properly execute a specific calligraphy style, you need to stick to it for an extended period, so it’s essential that you choose something that is appealing to you. </p>
                <p>Generally speaking, there are a few main categories of calligraphy styles.</p>
                <p>We can categorize calligraphy styles according to the type of pen we use. The main categories are – </p>
                <p>
                    <ul>
                        <li>Pointed nib calligraphy styles (Copperplate, Spencerian, etc.)</li>
                        <li>Broad-edged calligraphy styles (Uncial, Gothic, Foundational, etc.)</li>
                        <li>Brush pen calligraphy </li>
                    </ul>
                </p>
