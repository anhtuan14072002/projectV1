                <h4>What are some key features of calligraphy?</h4>
                <p>It’s important to distinguish between handwriting (including cursive) and calligraphy.</p>
                
                <p>It is common for the untrained eye to see these two as the same.</p>
                <p>With that in mind, here are a few key features of calligraphy. </p>
                <h4>1. Variation in stroke thickness.</h4>
                <p>Calligraphy can be done with any writing tool; however, there are specific tools dedicated to calligraphy.</p>
                <p>The two most common tools are broad-edged and pointed pens.</p>
                <p>These tools are specific for calligraphy because they can create stroke thickness variations.</p>
                <p>The broad-edged nib achieves that by keeping the pen at a consistent angle (and the direction of movement), whereas the pointed nib achieves that by adding/removing pressure as you write.</p>
                <p>The contrast in stroke thickness that these tools can create while you write is one of the most prominent features of calligraphic letters. </p>
                <img src="/images/li_calligraphy101_2.png" alt="">
                <h4>2. Letterform proportions and structure.</h4>
                <p>A specific proportion and structure of letters are maintained throughout the writing.</p>
                <p>This is particularly important for traditional calligraphy scripts.</p>
                <p>For example, Blackletter calligraphy is created with a broad-edged tool.</p>
                <p>The angle of the pen is held at a 45-degree angle.</p>
                <p>The letters are completely vertical, and they are proportionally sized according to the width of the pen.</p>
                <p>That’s why we use the pen width as a measuring unit when it comes to broad-edged (flat tip) calligraphy styles.</p>
                <p>These are just some of the “rules” you need to follow to create this particular style of calligraphy.</p>
                <p><b>Calligraphy guidelines</b> allow us to focus on creating beautiful and consistent letterforms.</p>
                <p>Without calligraphy guidelines, you would have to guess and eyeball all the sizing and proportions, and it would make the whole process much more difficult.</p>
                <img src="/images/li_calligraphy101_4.png" alt="">
                <h4>3. Consistency and uniformity.</h4>
                <p>Calligraphy places great emphasis on consistent and uniform letterforms.</p>
                <p>Achieving consistent stroke widths, angles, spacing, rhythm, etc., and overall letter shapes throughout a piece of writing is essential for creating a cohesive and visually pleasing result.</p>
                <p>In the example below, I am using two different colored brush pens along with the <b>basic calligraphy strokes</b> to write out the word minimum.</p>
                <p>I am focused on maintaining consistent spacing, slant, space within the letters (also known as counters), etc.</p>
                <img src="/images/li_calligraphy101_5.jpg" alt="">
                <p><i>The word “minimum” is a popular practice word for calligraphers since it contains many similar shapes.</i></p>
                <h4>What is calligraphy used for?</h4>
                <p>Today, calligraphy is used in many different ways.</p>
                <p>From personal use as a relaxing hobby and artistic expression to commercial purposes, official documents, wedding invitations, font designs, and much more. </p>
                <p><b>The written word can be used to convey not only information but also emotions.</b></p>
                <p>As you can see in the image below, the same sentence written in two different styles can communicate a very different feeling.</p>
                <img src="/images/li_calligraphy101_6.png" alt="">
                <p>Often we don’t pay any attention to letters used in advertisements, logotypes, and other promotional materials.</p>
                <p>Try to pay more attention the next time you’re in the city or any other place where you can see letters around you.</p>
                <p>Take a moment to look at the letters and think about what they communicate on an emotional level and how they make you feel.</p>
                <p>This could also be a helpful exercise to determine what sort of style of calligraphy you want to learn and practice.</p>
                <p>At this point, it’s also important to mention that calligraphy and hand lettering are not the same thing.</p>
                <p>Despite the fact that these two terms are often used interchangeably.</p>
                <p>When doing calligraphy, you’re <b>writing</b> letters, whereas, with hand lettering, you <b>draw</b> them.</p>
