                <h4>What is calligraphy? In simple words.</h4>
                <p>Calligraphy is a visual art form that creates decorative handwriting using specific tools.</p>
                <p>In other words, calligraphy is the art of beautiful writing.</p>
                <img src="/images/li_calligraphy101.png" alt="">
                <p>However, calligraphy is a broad term that encompasses a wide variety of styles and cultures from every corner of the globe. 
                </p>
                <p>We can primarily divide calligraphy into different cultures around the world.</p>
                <p>To name a few – </p>
                <ul>
                    <li>Western calligraphy (the Latin alphabet)</li>
                    <li>Chinese calligraphy</li>
                    <li>Japanese calligraphy</li>
                    <li>Arabic calligraphy</li>
                    <li>Indian calligraphy</li>
                </ul>
                <img src="/images/li_calligraphy101_1.png" alt="">
                <p><i><b>Note:</b> Despite the art of beautiful writing being present in many cultures across the globe, I focus primarily on studying, practicing, and teaching Western calligraphy and the Latin alphabet. </i></p>
