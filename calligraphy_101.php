                <h4>The Origins Of Calligraphy</h4>
                <p>Paleographers and historians believe the first alphabet evolved in Phoenicia around 1200 B.C.</p>
                <img src="/images/li_calligraphy101_8.png" alt="">
                <p>As Phoenicians merchants traveled and traded across the Mediterranean, the Greeks adopted their alphabet around the eighth century B.C.</p>
                <p>Soon after, the Etruscans adopted (and adapted) the Phoenician alphabet, which the Romans subsequently took.</p>
                <p>By the first century B.C., the Romans had already developed several scripts using the Latin alphabet, which we all know and use today.</p>
                <p>The word calligraphy derives from the Greek words “kallos” (beauty) and “graphein” (to write).</p>
                <p>According to Britannica.com, the word calligraphy entered most European languages by the end of the 16th century.</p>
                <p>This was initiated by the introduction of the printing press in the mid-15th century and thus created a clear distinction between handwriting and more elaborate forms of lettering.</p>
