                <h3>7. Spencerian Calligraphy </h3>
                <p>The Spencerian script is a calligraphy style dating from the 19th century in the United States. </p>
                <p>Developed by Platt Rogers Spencer, hence the name, Spencerian was so popular and widespread across the country that it became the official writing style in both personal correspondence as well as at a government level. </p>
                <img src="images\li-10style-22.png" alt="">
                <h4>Script characteristics </h4>
                <p>Unlike Copperplate, Spencerian is written at a slightly softer angle. </p>
                <p>Instead of a 55-degree, it’s a 52-degree slant. </p>
                <p>It’s also less shaded than Copperplate, giving it a more delicate look. </p>
                <p>The shade means the thicker downstroke. </p>
                <p>Another notable difference between the two scripts is that Copperplate is more oval-based, whereas Spencerian has a bit more of an angular look between the downstrokes and upstrokes. </p>
