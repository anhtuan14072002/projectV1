                <h3>5. Roman Capitals</h3>
                <img src="images\li-10style-17.png" alt="">
                <h4>Origins of the script</h4>
                <p>Roman Capitals date all the way back to the first century. </p>
                <p>They originated in Rome hence the name, and it is with this script that the journey of the Latin alphabet began. </p>
                <p>I find it absolutely fascinating the fact that these letterforms were developed by people nearly 2000 years ago and that they remained virtually unchanged until today. </p>
                <p>That such beauty, precision, and attention to detail existed back then. </p>
                <h4>Script characteristics </h4>
                <p>With Roman Capitals, we can make two clear distinctions – </p>
                <ul>
                    <li><b>Roman Square Capitals </b></li>
                    <li><b>Rustic Capitals</b></li>
                </ul>
                <p>The Roman Square Capitals is a geometrically based script where the widest letters (such as the O) fit in a perfect square, which gives it its distinctive name. </p>
                <img src="images\li-10style-18.png" alt="">
                <p>However, not all letters share the same proportions. </p>
                <p>Some of them fit in only half of the square shape, and the proportions of each letter are exactly predetermined. </p>
                <p>These letterforms are strong, wide, and precise, and I think they perfectly represent the Roman Empire. </p>
                <p>You will need a broad-edged tool to start practicing the Roman Square Capitals. </p>
                <p>It could be a nib, marker, or fountain pen. </p>
                <p>A flat brush is also an excellent tool for this script, considering the complex manipulations required to create the serifs. </p>
                <p>However, the best way to begin learning the Roman Square Capitals is by properly understanding the correct proportions of the letters using a pencil. </p>
                <p>Practicing the monoline skeleton of this calligraphy style will immensely improve your understanding and thus allow you to improve once you start using a broad-edged tool.</p>
                <h4>Rustic capitals</h4>
                <p>Although the Roman Square Capitals were a great representation of the might of the Roman Empire, it was a rather impractical script when it came to execution. </p>
                <p>Creating such beautiful letterforms takes time and effort, not to mention the ample space it takes. </p>
                <p>The Rustic Capitals was a script that solved this issue and had a more colloquial use. </p>
                <p>The Rustic Capitals are characterized by a narrower form achieved by a more narrow pen angle and a more relaxed hand movement. </p>
