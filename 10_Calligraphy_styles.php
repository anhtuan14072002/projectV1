                <h3>6. Foundational Hand</h3>
                <img src="images\li-10style-20.png" alt="">
                <h4>Origins of the script</h4>
                <p>The Foundational Hand was created in the 20th century by the famous calligrapher Edward Johnston. </p>
                <p>The Foundational Hand is a script based on the historical Carolingian minuscule from the 10th century. </p>
                <p>According to Johnston, the Foundational Hand is the best script for beginners to start practicing calligraphy. </p>
                <h4>Script characteristics</h4>
                <p>The Foundational Hand is a clean upright calligraphy script written with a broad-edge pen. </p>
                <p>A key focus letter of this style is the letter o since nearly all letters fit into that rounded shape. </p>
                <p>Like the Roman Square Capitals, the letter o of the Foundational Hand also fits proportionally into a square. </p>
                <p>The pen angle is mainly kept at 30 degrees, except for diagonal letters, where it’s turned to 45 degrees to balance the weight of the stroke. </p>
                <p>Such a pen angle gives us a very well-balanced look of thick and thin strokes across the letterforms.</p>
                <p>The Foundational calligraphy is sized with 4 nib widths and 2 additional nib widths for ascenders and descenders.</p>
