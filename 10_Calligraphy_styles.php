                <p>In short, modern calligraphy is any type of calligraphy that doesn’t strictly adhere to the rules of traditional scripts. </p>
                <p>This is a more suitable definition since modern calligraphy isn’t just bouncy brush calligraphy. It’s much more than that. </p>
                <p>Modern calligraphy can be created with virtually any writing tool you can think of. </p>
