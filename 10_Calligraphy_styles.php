                <h3>4. Uncial Calligraphy</h3>
                <img src="images\li-10style-14.png" alt="">
                <h4>Origins of the script</h4>
                <p>Uncial calligraphy evolved from the Roman capitals around the third century, with even some earlier historical records. </p>
                <p>As Christianity was gaining popularity, the Uncial letters served as the official script for their manuscript, which quickly found its way across Europe. </p>
                <p>Here is an absolutely gorgeous historical sample of the Half-Uncial script from the Book Of Kells (9th century) –</p>
                <img src="images\li-10style-15.png" alt="">
                <h4>Script characteristics </h4>
                <p>As with other traditional scripts, the Uncial comes in many variations.</p>
                <p>The three most notable variations of Uncial calligraphy are – </p>
                <ul>
                    <li><b>Uncial </b></li>
                    <li><b>Half-Uncial</b></li>
                    <li><b>Artificial Uncial</b></li>
                </ul>
                <p>Another distinct feature of the Uncial script was that although it was a pure capital letter script (majuscules), it was the script that first introduced some of the lowercase letters we know and use today. </p>
                <p>The Half-Uncial lowercase letters were even more prominent than the Uncial script. </p>
                <p>It also differed from its predecessor (Roman capitals) by an absence of serifs. </p>
                <p>The Uncial style is usually created using a broad-edge calligraphy tool (nib, marker, fountain pen, brush).</p>
                <p>It is commonly sized with 4 nib widths and an extra nib width for the slight extensions of specific letters.</p>
                <p>Fun Fact: Calligrapher Daniel Reeve used the Uncial script to develop one of the fonts used in the Lord Of The Rings movies. </p>
                <p>\A subsequent form of the Uncial is the Artificial Uncial, a more elaborate form characterized by serifs resembling the Roman Capitals. </p>
                <p>You could say it’s a more elegant-looking Uncial that requires a higher level of skill with the pen. </p>
                <p>If Uncial calligraphy is something you would like to learn, I suggest starting with the classic form of Uncial before attempting the Artificial Uncial. </p>
