                <h3>9. Neuland Calligraphy</h3>
                <img src="images\li-10style-27.png" alt="">
                <h4>Origins of the script</h4>
                <p>The Neuland script was created in the 20th century by German type designer and calligraphy Rudolf Koch.</p>
                <p>Neuland was initially created as a typeface and, in later years, adopted by calligraphers as a writing alphabet.</p>
                <p>Along with Edward Johnston, Rudolf Koch was also considered to be at the forefront of the revival of calligraphy as an art form during the 20th century. </p>
                <h4>Script characteristics</h4>
                <p>What makes this calligraphy style so unique is its simplicity and boldness. In other words, it’s funky and chunky. </p>
                <p>It’s a complete majuscule script (only capitals), and it has a distinctive playfulness. </p>
                <p>You will also notice that there is a lack of contrast between the thick and thin parts, as you can see with other broad-edged scripts. </p>
