                <h3>2. Blackletter calligraphy</h3>
                <img src="images\li-10style-4.png" alt="">
                <h4>Origins of the script</h4>
                <p>Think of Blackletter (gothic) calligraphy as a family of different scripts.</p>
                <p>There are so many different blackletter calligraphy styles that you can practice.</p>
                <p>They all have their own names, history, and little nuances. </p>
                <p>It would be quite impractical to try and cover all of them in a single article, so I decided to go over the most popular ones – </p>

                <ul>
                    <li><b>Textura Quadrata</b></li>
                    <li><b>Fraktur</b></li>
                    <li><b>Rotunda </b></li>
                    <li><b>Batarde</b></li>
                </ul>

                <p>Each of these styles emerged in different time periods and locations across Europe.</p>
                <p>However, to generalize, blackletter calligraphy emerged in Northern Europe around the 11th century. </p>
                <img src="images\li-10style-5.png" alt="">
                <h4>Script characteristics </h4>
                <p>Thick, black, dense, vertical strokes with sharp edges characterize black letter calligraphy. </p>
                <p>Blackletter calligraphy is a very expressive calligraphy style (regardless of the specific script), and today, there are so many variations out there that you can practice. </p>
                <p>Blackletter calligraphy is practiced using a broad-edged nib, marker, or fountain pen. </p>
                <p>The minuscules are usually sized at 5 nib widths, while the ascenders, descenders, and capitals are at 7 nib widths.</p>
                <p>However, some blackletter calligraphy scripts use different sizing proportions (i.e., the Batarde script).</p>
                <img src="images\li-10style-6.png" alt="">
                <p>
                Unlike Copperplate calligraphy, where the thickness difference is created by adding/releasing pressure from the pen, the broad-edged nib creates variations in thickness by holding the nib at a consistent angle. 
                </p>

