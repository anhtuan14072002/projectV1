                <h3>8. Gothicized Italic</h3>
                <img src="images\li-10style-24.png" alt="">
                <h4>Origins of the script</h4>
                <p>This is probably one of my favorites from this list. </p>
                <p>It combines two of my very dear calligraphy styles – blackletter (gothic) and italic. </p>
                <p>Gothicized Italic as a script was developed by famous calligrapher Edward Johnston. </p>
                <h4>Script characteristics </h4>
                <p>This style is so particular because it retains the strength and rigidity of blackletter calligraphy. Still, simultaneously, the curves taken from the italic script add more rhythm, which also improves the legibility. </p>
                <p>It’s like mixing PB & J. It’s a fantastic combination that everyone loves!</p>
                <p>Gothicized Italic is created with a broad-edged pen. Again, that could be a nib, marker, or the popular Parallel pen. </p>
                <p>The proportions of Gothicized Italic are the same as for blackletter calligraphy.</p>
                <p>An upright slant, an x-height of 5 nib-widths, and 2 extra nib widths for the ascending and descending letters.  </p>
