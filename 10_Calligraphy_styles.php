                <h3>3. Italic calligraphy</h3>
                <img src="images\li-10style-11.png" alt="">
                <h4>Origins of the script</h4>
                <p>Also known as the Cancelleresca script, Italic calligraphy is a beautiful script that evolved from the Humanist minuscule and originated in Italy during the Renaissance. </p>
                <p>If you’re interested in historical examples of this beautiful script in action, I highly recommend checking out La Operina by Ludovico Vicentino degli Arrighi.</p>
                <img src="images\li-10style-12.png" alt="">
                <p><b>Note - </b>It is incorrect to call calligraphy styles or scripts – calligraphy fonts. Fonts are what computers and other devices use to display text. Calligraphy styles are either called styles or scripts. </p>
                <h4>Script characteristics </h4>
                <p>Best recognized for its smooth and elegant strokes with a slight forward slant. </p>
                <p>It’s a very rhythmic and dynamic calligraphy style that retains a high level of readability. </p>
                <p>The Italic script is practiced with a broad-edged tool. That can be a broad-edged marker, nib + ink, or a fountain pen (Pilot Parallel Pen). </p>
                <p>Written at a slant between 5 and 10 degrees, the letters are usually taller than wide.</p>
                <p>Minuscules are usually sized at 5 nib widths while ascending and descending letters are extended equal to the x-height but can be shorter or longer if needed.</p>
                <p>The capitals (or majuscules) are slightly lower at 7 nib widths.</p>
