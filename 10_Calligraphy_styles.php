                <h3>1. Copperplate calligraphy</h3>

                <img src="images\li-10style.jpg" alt="">
                <h3>Origins of the script </h3>
                <p>The Copperplate script can be traced back to the late 17th century. Back then, known under a different name – the English Round Hand.</p>
                <p>Without going too deep into the whole history of the Copperplate script, the name was adopted later on as calligraphers tried to copy the beautiful letters engraved on copper plates created by the penmanship masters of that time. </p>
                <p>I talk more about this in my <b>article about the history of calligraphy. </b></p>
                <p>However, for a more in-depth explanation of the origins of the Copperplate script, I highly </p>
                <p>recommend checking out this <b>fantastic article</b> by Sybille.</p>
                <img src="images\li-10style-1.png" alt="">
                <h6><b>George Bickham’s</b> Round Hand script, from The Universal Penman, c. 1740–1741 (an engraving, not actual writing). Source - <b>Wikipedia.com {{PD-US}}</b></h6>
                
                <h3>Script characteristics </h3>
                <p>The Copperplate script is undoubtedly one of the most popular calligraphy styles worldwide. Highly slanted, elegant, flowy, delicate, and precise strokes are just some elements that make Copperplate such an attractive style. </p>
                <p>The Copperplate script is mainly created with a pointed nib and ink. </p>
                <p>However, you can also practice it using a brush, pen, or pencil. </p>
                <p>As mentioned, it is a highly slanted style angled at 55 degrees.</p>
                <p>The x-height of letters isn’t fixed, meaning you can determine how big you want your minuscule to be.</p>
                <p>However, when practicing, it’s not practical to have a large x-height (especially when working with a dip pen and ink).</p>
                <p>The ascenders and descenders are one and a half times the x-height, making them proportional regardless of the size you choose to write your letters.</p>
                <img src="images\li-10style-1.png" alt="">
                <p>The best way to begin practicing the Copperplate script is by learning the basic strokes and understanding the proper sizing and proportions of the letters.</p>
                <p>The key is forming strong foundations that will allow you to create letters, words, and even intricate flourishing compositions. </p>

